function generateColumnDefs(data: any[]): ColumnDef<TOrderDetails>[] {
  if (!data || !data.length) {
    return [];
  }

  const firstItem = data[0];
  const keys = Object.keys(firstItem);

  const columns = keys.map((key) => ({
    accessorKey: key,
    header: key.toLowerCase(),
    cell: ({ row }: { row: any }) => {
      const value = row.getValue(key) as any;
      return (
        <div
          className={`group text-xs  h-min w-full whitespace-nowrap  relative `}
        >
          <div className="absolute -top-10 right-0 p-2 hidden group-hover:inline-flex bg-input rounded-lg ease-in-out duration-75">
            ID: {row.getValue("ORDER_ID")} | {row.getValue("CHANNEL_NAME")}
          </div>
          <span
            contentEditable
            className="p-2 focus:outline-none focus:border-b border-foreground/50 "
          >
            {value}
          </span>
        </div>
      );
    },
  }));

  return [
    {
      id: "select",
      header: ({ table }) => (
        <Checkbox
          checked={
            table.getIsAllPageRowsSelected() ||
            (table.getIsSomePageRowsSelected() && "indeterminate")
          }
          onCheckedChange={(value) => table.toggleAllPageRowsSelected(!!value)}
          aria-label="Select all"
        />
      ),
      cell: ({ row }) => (
        <Checkbox
          checked={row.getIsSelected()}
          onCheckedChange={(value) => row.toggleSelected(!!value)}
          aria-label="Select row"
        />
      ),
      enableSorting: false,
      enableHiding: false,
    },
    ...columns,
  ];
}

